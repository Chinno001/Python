#Clases de HTML:
#Se pueden usar para definir atributos en las etiquetas, lo que permite aplicar estilos o comportamientos específicos a los elementos que pertenecen a esa clase.
#Por ejemplo, si queremos que todos los elementos con la clase "resaltado" tengan un fondo amarillo, podríamos definir esa clase y luego asignar esa clase a los elementos que queremos resaltar con amarillo.


#Componentes de un estilo CSS: 
#1. Selector: Es la etiqueta, clase o id que se utiliza para seleccionar el elemento al que se le quiere aplicar el estilo.
#2. Propiedad: Es la característica del elemento que se quiere modificar, como el color, el tamaño o la fuente.
#3. Valor: Es el valor que se le asigna a la propiedad, como "red" para el color o "16px" para el tamaño.


#Las propiedades de estilo puedo llamarlas de diferentes maneras, con #, con punto o sin nada, dependiendo de si quiero llamar a una etiqueta, clase o id respectivamente.
#Una etiqueta se llama sin nada, solamente con el nombre de la etiqueta, como "div" o "p".
#Una clase se llama con punto (.)
# y un id se llama con (#).


